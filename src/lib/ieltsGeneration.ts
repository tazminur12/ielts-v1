/**
 * Last-resort cleanup if the model leaks obvious automation phrases (does not rewrite style).
 */
export function sanitizeIeltsCandidateText(text: string): string {
  if (!text || typeof text !== "string") return text;
  let t = text;
  const removals: RegExp[] = [
    /\bAs an AI (?:language )?model,?\s*/gi,
    /\bI am (?:an )?AI[,.\s]*/gi,
    /\bI'm (?:an )?AI[,.\s]*/gi,
    /\bThis (?:content|text|passage) (?:was|is) (?:AI[- ]?)?generated\b[^.]*\.?\s*/gi,
    /\bgenerated by (?:ChatGPT|OpenAI|an AI|artificial intelligence)\b[^.]*\.?\s*/gi,
    /\bChatGPT\b/gi,
  ];
  for (const re of removals) {
    t = t.replace(re, "");
  }
  return t.replace(/\s{2,}/g, " ").replace(/^\s+|\s+$/g, "").trim();
}
